private void limpiar() {                                          

        cprodtxt.setText("");
        nomprodtxt.setText("");
        provtxt.setText("");
        canttxt.setText("");
        marctxt.setText("");
     
        cprodtxt.requestFocus();      
}

private void bloquear() { 
    
        nomprodtxt.setEnabled(false);
        provtxt.setEnabled(false);
        canttxt.setEnabled(false);
        marctxt.setEnabled(false);
        
        registrar.setEnabled(false);
      
}

private void desbloquear() { 
    
        nomprodtxt.setEnabled(true);
        provtxt.setEnabled(true);
        canttxt.setEnabled(true);
        marctxt.setEnabled(true);
        
        registrar.setEnabled(true);
      
}

private void desbloquear2() { 
   
        canttxt.setEnabled(true);
        
        registrar.setEnabled(true);
        canttxt.requestFocus();
}

